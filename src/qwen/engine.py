import copy
import functools
import logging
import torch
import asyncio
from typing import AsyncIterator

from qwen.model import QwenForCausalLM
from qwen.cache import init_kv_cache2
from qwen.sampling import SamplingMetadata
from qwen.cache import KVCache
from qwen.attention import build_prefill_metadata, build_decode_metadata

logger = logging.getLogger(__name__)


def _prefill_step(model, mutable_prompt_ids, B, cache,
                    prompt_ids, output_ids, sampling_meta, finished):
    packed_input_ids, md = build_prefill_metadata(mutable_prompt_ids, cache, model.device, model.config.cache_len)  # is_prefill=True
    hidden = model.forward(packed_input_ids, md)       # [total_tokens, H]

    # gather each seq's LAST token -> logits -> first generated token
    last_idx = md.cu_seqlens_q[1:] - 1          # [B]
    logits = model.compute_logits(hidden[last_idx])   # [B, vocab]
    next_tokens = model.sampler(logits, prompt_ids, output_ids, sampling_meta)          # [B]

    # freeze finished rows: don't record, don't let them emit real tokens downstream
    # _prefill_step will run until finished in the non-cache scenario
    newly = (next_tokens in model.config.eos_token_id) & ~finished
    for i in range(B):
        if not finished[i]:
            output_ids[i].append(next_tokens[i].item())
            mutable_prompt_ids[i].append(next_tokens[i].item())
    finished |= newly

    past_lens = md.cache_seqlens    # update past_lens with new cache_seqlens generated by input_ids
    return past_lens, finished, next_tokens, output_ids, mutable_prompt_ids

def _decode_step(model, next_tokens, B, cache, cache_len, prompt_ids, output_ids, sampling_meta, past_lens, finished):
    md = build_decode_metadata(past_lens, cache, cache_len)   # is_prefill=False, fresh each step
    hidden = model.forward(next_tokens, md)  # input_ids = [B], one token per seq -> list[int]
    logits = model.compute_logits(hidden)    # [B, vocab]  (decode: every row is a last token)
    next_tokens = model.sampler(logits, prompt_ids, output_ids, sampling_meta)      # [B]

    # freeze finished rows: don't record, don't let them emit real tokens downstream
    newly = (next_tokens in model.config.eos_token_id) & ~finished
    for i in range(B):
        if not finished[i]:
            output_ids[i].append(next_tokens[i].item())
    finished |= newly

    past_lens = md.cache_seqlens    # update past_lens by cache_seqlens( = past_lens + 1 in build_decode_metadata)
    return past_lens, finished, next_tokens, output_ids

@torch.inference_mode()
def generate(
    model: QwenForCausalLM,
    input_ids: list[list[int]],    # raw tokenizer output, list of variable-length id sequences
    max_new_tokens: int = 300,
    temperature: float | None = None,
    use_cache: bool = True,
    cache_len: int = 0,            # defaults to model.config.cache_len
) -> list[list[int]]:
    prompt_ids = input_ids
    B = len(prompt_ids)
    sampling_meta = SamplingMetadata(config=model.config, bsz=B, temperature=temperature)

    max_input_seq_length = max(len(s) for s in prompt_ids)
    cache_len = min(cache_len, model.config.cache_len) if use_cache and cache_len > 0 else model.config.cache_len
    max_new_tokens = min(max_new_tokens, cache_len - max_input_seq_length)
    if use_cache:
        cycles_prefill_loop, cycles_decode_loop = 1,  max_new_tokens- 1
        cache = init_kv_cache2(model.config, B, model.config.cache_len-max_input_seq_length)
    else:
        cycles_prefill_loop, cycles_decode_loop = max_new_tokens, 0
        cache = None

    # persistent state, lives across the whole generation
    past_lens = torch.zeros(B, dtype=torch.int32, device=model.device)
    finished = torch.zeros(B, dtype=torch.bool,  device=model.device)
    output_ids = [[] for _ in range(B)]           # collected token ids per seq

    # PREFILL, loop only for non-cache
    mutable_prompt_ids = copy.deepcopy(input_ids)
    for _ in range(cycles_prefill_loop):
        past_lens, finished, next_tokens, output_ids, mutable_prompt_ids = \
            _prefill_step(
                model=model, mutable_prompt_ids=mutable_prompt_ids, B=B, cache=cache,
                prompt_ids=prompt_ids, output_ids=output_ids,
                sampling_meta=sampling_meta, finished=finished,
            )

        if finished.all():
            break

    # DECODE loop
    for _ in range(cycles_decode_loop):
        if finished.all():
            break

        past_lens, finished, next_tokens, output_ids = \
            _decode_step(
                model=model, next_tokens=next_tokens, B=B, cache=cache, cache_len=cache_len,
                prompt_ids=prompt_ids, output_ids=output_ids,
                sampling_meta=sampling_meta, past_lens=past_lens, finished=finished,
            )

    return [prompt_id + output_id for prompt_id, output_id in zip(prompt_ids, output_ids)]

@torch.inference_mode()
async def async_generate(
    model: QwenForCausalLM,
    input_ids: list[list[int]],    # raw tokenizer output, list of variable-length id sequences
    max_new_tokens: int = 300,
    temperature: float | None = None,
    use_cache: bool = True,
    cache_len: int = 0,            # defaults to model.config.cache_len
) -> AsyncIterator[list[list[int]]]:
    prompt_ids = input_ids
    B = len(prompt_ids)
    sampling_meta = SamplingMetadata(config=model.config, bsz=B, temperature=temperature)

    max_input_seq_length = max(len(s) for s in prompt_ids)
    cache_len = min(cache_len, model.config.cache_len) if use_cache and cache_len > 0 else model.config.cache_len
    max_new_tokens = min(max_new_tokens, cache_len - max_input_seq_length)
    if use_cache:
        cycles_prefill_loop, cycles_decode_loop = 1,  max_new_tokens- 1
        cache = init_kv_cache2(model.config, B, model.config.cache_len-max_input_seq_length)
    else:
        cycles_prefill_loop, cycles_decode_loop = max_new_tokens, 0
        cache = None

    loop = asyncio.get_running_loop()

    # persistent state, lives across the whole generation
    past_lens = torch.zeros(B, dtype=torch.int32, device=model.device)
    finished = torch.zeros(B, dtype=torch.bool,  device=model.device)
    output_ids = [[] for _ in range(B)]           # collected token ids per seq

    # output prompt IDs first
    yield prompt_ids

    # PREFILL, loop only for non-cache
    mutable_prompt_ids = copy.deepcopy(input_ids)
    for _ in range(cycles_prefill_loop):
        past_lens, finished, next_tokens, output_ids, mutable_prompt_ids = \
            await loop.run_in_executor(None, 
                functools.partial(
                    _prefill_step,
                    model=model, mutable_prompt_ids=mutable_prompt_ids, B=B, cache=cache,
                    prompt_ids=prompt_ids, output_ids=output_ids,
                    sampling_meta=sampling_meta, finished=finished,
                )
            )
        yield [ output_id[-1:] for output_id in output_ids ]

        if finished.all():
            break

    # DECODE loop
    for _ in range(cycles_decode_loop):
        if finished.all():
            break

        past_lens, finished, next_tokens, output_ids = \
            await loop.run_in_executor(None, 
                functools.partial(
                    _decode_step,
                    model=model, next_tokens=next_tokens, B=B, cache=cache, cache_len=cache_len,
                    prompt_ids=prompt_ids, output_ids=output_ids,
                    sampling_meta=sampling_meta, past_lens=past_lens, finished=finished,
                )
            )
        yield [ output_id[-1:] for output_id in output_ids ]
